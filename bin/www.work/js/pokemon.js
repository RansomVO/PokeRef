// The value passed to the other GetPokemon*() methods may pass in the table cell instead of the actual Pokémon, so get the Pokemon
function GetPokemon(pokemon) {
    if (pokemon.tagName === 'TD') {
        for (var i = pokemon.childNodes.length - 1; i >= 0; i--) {
            if (pokemon.childNodes[i].nodeName === 'DIV' && pokemon.childNodes[i].classList.contains('SPRITE_FILLER')) {
                return pokemon.childNodes[i];
            }
        }
    }

    return pokemon;
}


